class Iteration {
  public static void main(String[] args) {

    // for loop
    for (int i = 0; i < 5; i++) {
      System.out.println("FOR-LOOP: Hello, World!");
    }

    // while loop
    int i = 0;
    while (i < 5) {
      System.out.println("WHILE-LOOP: Hello, World!");
      i++;
    }

    // do-while loop
    i = 0;
    do {
      System.out.println("DO-WHILE-LOOP: Hello, World!");
      i++;
    } while (i < 5);

  }
}
